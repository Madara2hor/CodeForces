import Foundation

extension String {
    
    static let empty = ""
    static let space = " "
    static let undeline = "_"
    static let newLine = "\n"
}

extension Int {
    
    static let one = 1
    static let two = 2
}

extension CGFloat {
    
    static let one: CGFloat = 1.0
    static let two: CGFloat = 2.0
    static let three: CGFloat = 3.0
    static let half: CGFloat = 0.5
}
